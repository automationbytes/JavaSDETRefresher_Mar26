package org.example;

public class ComparisonOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 8;

        //equal
        System.out.println(a == b);

        //not equal
        System.out.println(a != b);

        //greater than

        System.out.println(a > b);

        //lesser than
        System.out.println(a < b);
    }
}
